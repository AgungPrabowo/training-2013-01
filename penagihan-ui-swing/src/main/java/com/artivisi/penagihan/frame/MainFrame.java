    private void setSelectedPanel(String panelName) {
        tbdPane.setSelectedComponent(tbdPane
                .getComponentAt(getComponentIndexByName(panelName)));
    }

    private int getComponentIndexByName(String panelName) {
        return tbdPane.indexOfTab(panelName);
    }
 
   public JTabbedPane getTbdPane() {
        return tbdPane;
    }
    
    private void initNasabahTab() {
        PanelNasabah panelNasabah = new PanelNasabah(penagihanService);
        panelNasabah.setName("Nasabah");
        
        indexTab = getComponentIndexByName("Nasabah");
        if(indexTab == -1) {
            tbdPane.addTab("Nasabah", panelNasabah);
            setSelectedPanel("Nasabah");
        } else {
            tbdPane.setSelectedIndex(indexTab);
        }
    }
    
    private class TreeSelectListener implements TreeSelectionListener {

        @Override
        public void valueChanged(TreeSelectionEvent tse) {
            DefaultMutableTreeNode node = (DefaultMutableTreeNode) jTree1.getLastSelectedPathComponent();

            if (node == null) {
                return;
            }
            Object nodeInfo = node.getUserObject();
            menu = (Menu) nodeInfo;
            
            if(menu.getNomer().equals("112")){
                initNasabahTab();
            } 
        }
    }

    
